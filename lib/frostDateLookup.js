export function getZoneForZipPrefix(prefix) {
  const zoneKey = ZIP_PREFIX_ZONES[prefix]
  if (zoneKey && ZONE_FROST_DATES[zoneKey]) {
    return {
      zone: zoneKey,
      ...ZONE_FROST_DATES[zoneKey],
    }
  }
  return null
}

export function getZoneForZip(zip) {
  if (!zip || zip.length < 3) return null
  const prefix = zip.substring(0, 3)
  return getZoneForZipPrefix(prefix)
}
